using System;

class DeclareVariables
{
    static void Main()
    {
        ushort firstVariable = 52130;
        sbyte secondVariable = -115;
        int thirdVariable = 4825932;
        byte fourthVariable = 97;
        short fifthVariable = -10000;

        Console.WriteLine("The first number : " + firstVariable);
        Console.WriteLine("The second number: " + secondVariable);
        Console.WriteLine("The third number : " + thirdVariable);
        Console.WriteLine("The fourth number: " + fourthVariable);
        Console.WriteLine("The fifth number : " + fifthVariable);
    }
}

